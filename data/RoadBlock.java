	public boolean isHits(Rectangle rect, int sX, int sOldX, int sY, int sOldY) //tankÒÆ¶¯Åö×²¼ì²â
	{
		WallNode wn = null;
		for(int i=0;i<wallNodesList.size();i++)
		{
			wn = wallNodesList.get(i);
			
			double distance = Math.sqrt((wn.x-sX)*(wn.x-sX) + (wn.y-sY)*(wn.y-sY));
			double oldDistance = Math.sqrt((wn.x-sOldX)*(wn.x-sOldX) + (wn.y-sOldY)*(wn.y-sOldY));
				
			if(rect.intersects(wn.getRect()) && distance<=oldDistance)
			{
				return true;
			}
		}
		return false;
	}
	
	public boolean isHits(Shot s) //×Óµ¯ÆÆ»µÇ½
	{
		WallNode wn = null;
		boolean isHit = false;
		
		ArrayList<Integer> wNNumber = new ArrayList<Integer>();
		
		for(int i=0;i<wallNodesList.size();i++)
		{
			wn = wallNodesList.get(i);
			if(s.getHitWallRect().intersects(wn.getRect()))
			{
				switch(wn.rigidity)
				{
				case 1:
					wn.isLive = false;
					wNNumber.add(i);
					break;
				case 2:
					if(s.tk.level>2)
					{
						wn.isLive = false;
						wNNumber.add(i);
					}
					break;
				case 3:
					if(s.tk.level>4)
					{
						wn.isLive = false;
						wNNumber.add(i);
					}
					break;
				default:break;
				}
				isHit = true;
				if(wallNodesList.size()==0) //Ç½È«²¿±»ÆÆ»µÁË
				{
//					this.isLive = false;
					this.fp.roadBlockList.remove(this);
//					System.out.println(fp.wallList.size());
				}
			}
		}
		
		if(isHit)
		{
			int number;
			for(int i=0,j=0; i<wNNumber.size();i++,j++)
			{
				number = wNNumber.get(i);
				wallNodesList.remove(number-j);
			}
			wNNumber.clear();
			
			return true;
		}else{
			return false;
		}
	}
